#Даден е списък numbers = [5, 1, 8, 2, 8, 3]. Премахнете дубликатите и го сортирайте във възходящ ред.

# Даден списък с числа
first_list = [5, 1, 8, 2, 8, 3]

# Преобразуваме в set за премахване на дубликатите
my_set = set(first_list)

# Преобразуваме обратно в списък и сортираме
second_list = sorted(list(my_set))

# Извеждаме резултатите
print(f"Първоначалният списък беше: {first_list}")
print(f"Сортирания списък без дубликати е : {second_list}")
