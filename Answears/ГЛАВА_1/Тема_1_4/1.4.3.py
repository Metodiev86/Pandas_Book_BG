#Създайте кортеж с три имена на града. Опитайте се да промените един от елементите (трябва да получите грешка).

# Създаване на кортеж с имена на градове
tuple_city = ("София", "Пловдив", "Варна")

# Опит за промяна на елемент на кортежа
try:
    tuple_city[2] = "Бургас"  # Това ще доведе до грешка
except TypeError as e:
    print(f"Грешка: {e}")  # Ще хванем грешката и ще я изведем

# Получаваме грешка: TypeError: 'tuple' object does not support item assignment
