#2.	Напишете код, който създава нов текстов файл с име new_file.txt и записва в него следния текст:
# Това е първият ред.
# Това е вторият ред.

# Опитваме се да създадем и запишем текстов файл
try:
    # Използваме with, за да отворим файла безопасно в режим "write" (записване)
    # Ако файлът не съществува, ще бъде създаден; ако съществува, ще бъде презаписан
    with open("my_file2.txt", "w", encoding="utf-8") as file:
        # Записваме първия ред в текстовия файл, като добавяме символ за нов ред '\n'
        file.write("Това е първият ред.\n")
        # Записваме втория ред
        file.write("Това е вторият ред.\n")

# Използваме глобално прихващане на грешки, което ще хване всяко изключение (не само конкретно)
except Exception as e:
    # Ако възникне грешка по време на отваряне или запис във файла, тя ще бъде отпечатана тук
    print("Грешка при работа с файла:", e)
