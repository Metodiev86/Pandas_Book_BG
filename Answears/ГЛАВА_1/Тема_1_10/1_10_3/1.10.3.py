#Напишете код, който отваря текстов файл с име my_file.txt в режим за четене и извежда съдържанието му на конзолата. Представете си, че файлът съдържа няколко реда текст.

# Отваряме файл с име "my_file.txt" в режим за четене ("r") и указваме, че използваме UTF-8 кодировка
with open("my_file.txt", "r", encoding="utf-8") as file:
    # Обхождаме всеки ред от файла
    for line in file:
        # Отпечатваме реда. Редовете вече съдържат символ за нов ред (\n), така че print не добавя допълнително
        print(line, end='')  # end='' предотвратява добавянето на допълнителен празен ред


