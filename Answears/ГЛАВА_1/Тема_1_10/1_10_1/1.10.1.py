# Функция, която се опитва да отвори файл по подаден път и обработва грешки
def open_file(file_path: str):
    try:
        # Опитваме се да отворим файла в режим "read" (само за четене)
        file = open(file_path, "r")
        print(f"Файлът '{file_path}' е отворен успешно.")
    except FileNotFoundError:
        # Ако файлът не съществува, се хваща грешката и се извежда съобщение
        print(f"ГРЕШКА: Файлът '{file_path}' не съществува.")
    finally:
        # Блокът finally винаги се изпълнява
        try:
            # Опитваме се да затворим файла, ако е бил успешно отворен
            file.close()
        except NameError:
            # Ако 'file' не е дефиниран (т.е. не е бил отворен), игнорираме грешката
            pass

# Тест 1: Файл, който предполагаемо съществува (трябва да съществува в директорията)
