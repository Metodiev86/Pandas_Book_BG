#Напишете код, който оперира с две променливи със стойности избрани от вас числа иизвежда техния сбор, разлика, произведение и частно (ако е възможно).

# Дефинираме две цели числа
m = 10
n = 5

# Събиране
print(f"{m} + {n} = {m + n}")  # Сбор

# Изваждане
print(f"{m} - {n} = {m - n}")  # Разлика

# Умножение
print(f"{m} * {n} = {m * n}")  # Произведение

# Деление с проверка дали n ≠ 0
if n != 0:
    print(f"{m} / {n} = {m / n:.2f}")  # Частно, форматирано до 2 знака
else:
    print("Грешка: Опит за деление на 0.")
