#Дадени са две булеви променливи a = True и b = False. Изведете резултата от a and b, a or b и not a.

# Дефинираме две булеви стойности
a = True
b = False

# Логическо AND – връща True, ако и a и b са True
print(f"a and b = {a and b}")

# Логическо OR – връща True, ако поне едно от двете е True
print(f"a or b = {a or b}")

# Логическо NOT – връща обратното на стойността
print(f"not a = {not a}")
print(f"not b = {not b}")
