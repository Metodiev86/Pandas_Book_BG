#Напишете код, който приема булева стойност и я преобразува към низ. Изведете резултата и неговия тип.

# Дадена булева стойност
bool_variable = True

# Преобразуване на булевата стойност към низ (стринг)
str_bool_variable = str(bool_variable)

# Извеждане на оригиналната стойност и нейния тип
print(f"bool_variable = {bool_variable}, тип: {type(bool_variable)}")

# Извеждане на преобразуваната стойност и нейния тип
print(f"str_bool_variable = \"{str_bool_variable}\", тип: {type(str_bool_variable)}")


