# Създайте низ "Python е мощен език". Изведете дължината на низа, първия и последния му символ. Извлечете подниза
# "мощен език".

# Дефинираме примерен низ
my_string = "Python е мощен език"

# 1. Извеждаме дължината на низа с функцията len()
print(f"Дължината на низа е: {len(my_string)} символа")

# 2. Извеждаме първия и последния символ с индекси
print(f'Първият символ е: "{my_string[0]}", а последният е: "{my_string[-1]}"')

# 3. Извличаме подниза "мощен език" чрез срез (slice)
substring = my_string[9:]  # Започваме от индекс 9 до края
print(f'Извлечен подниз: "{substring}"')

#4. Автоматично намиране на началото на подниза
start_index = my_string.find("мощен")
print(f'Автоматично извлечен подниз: "{my_string[start_index:]}"')
