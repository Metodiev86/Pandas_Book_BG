#Създайте променлива, съдържаща цяло число, друга - с десетично число, трета - с низ и четвърта - с булева стойност. Изведете типа на всяка променлива.

# Създаваме променлива с цяло число
int_variable = 10

# Създаваме променлива с десетично (float) число
float_variable = 3.5

# Създаваме променлива с низ (string)
string_variable = "Здравей"

# Създаваме променлива с булева стойност (True/False)
bool_variable = True

# Извеждаме типа на всяка променлива с помощта на функцията type()
print(f"Типът на int_variable е {type(int_variable)}")        # <class 'int'>
print(f"Типът на float_variable е {type(float_variable)}")    # <class 'float'>
print(f"Типът на string_variable е {type(string_variable)}")  # <class 'str'>
print(f"Типът на bool_variable е {type(bool_variable)}")      # <class 'bool'>

