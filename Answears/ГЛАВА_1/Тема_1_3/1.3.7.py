#Напишете код, който приема цяло число и го преобразува към двоичен, осмичен и шестнадесетичен низ. Изведете
# резултатите.

# Дадено цяло число
n = 47

# Преобразуване в различни бройни системи
binary_n = bin(n)       # Двоично представяне: връща '0b101111'
octal_n = oct(n)        # Осмично представяне: връща '0o57'
hex_n = hex(n)          # Шестнадесетично представяне: връща '0x2f'

# Извеждане на стойностите без префикси и с пояснения
print(f"n в десетична бройна система е: {n}")
print(f"n в двоична бройна система е: {binary_n[2:]}")        # Премахваме '0b'
print(f"n в осмична бройна система е: {octal_n[2:]}")         # Премахваме '0o'
print(f"n в шестнадесетична бройна система е: {hex_n[2:].upper()}")  # Премахваме '0x' и правим буквите главни

#Ако искаш да запазиш префиксите, можеш просто да ги покажеш така:

print(f"Двоично: {binary_n}, Осмично: {octal_n}, Шестнадесетично: {hex_n.upper()}")
