#Напишете код, който приема низ, представляващ цяло число в двоична система (например "1101"), и го преобразува към
# десетично цяло число. Изведете резултата.

# Двоичен низ, представящ цяло число
binar_str = "1001"  # Това е 9 в десетична бройна система

# Преобразуване от двоичен (основа 2) към десетично цяло число
deci_str = int(binar_str, 2)

# Извеждане на входния низ и получения резултат
print(f"binar_str = {binar_str} (двоично)")
print(f"deci_str = {deci_str} (десетично)")


#Ако искаш да избегнеш грешка при невалиден двоичен низ, добави проверка:
try:
    deci_str = int(binar_str, 2)
    print(f"deci_str = {deci_str}")
except ValueError:
    print("Невалиден двоичен низ!")
