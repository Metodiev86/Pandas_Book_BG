#Инсталирайте външния пакет requests с помощта на pip. След това напишете кратък код, който използва requests за да направи GET заявка към "https://www.example.com" и изведе статус кода на отговора. (Уверете се, че имате интернет връзка).

# Импортиране на външния модул requests.
# Ако не е инсталиран, го инсталирай с: pip install requests
import requests

# URL адресът, към който ще направим GET заявка
url = "https://www.example.com"

try:
    # Изпращаме GET заявка към зададения URL
    response = requests.get(url)

    # Ако заявката е успешна, отпечатваме статус кода на отговора
    # Статус код 200 означава "OK" (успешна заявка)
    print(f"Статус код на отговора: {response.status_code}")

except requests.exceptions.RequestException as e:
    # Ако възникне някаква грешка (например: няма интернет, лош URL и др.),
    # тя ще бъде уловена тук и ще я отпечатаме
    print(f"Възникна грешка при заявката: {e}")
