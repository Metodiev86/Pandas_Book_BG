#Напишете програма, която използва модула datetime за да:
# o	Изведе текущата дата и час.
# o	Създаде обект date за дадена дата (например 25 декември 2024 г.).
# o	Изчисли разликата в дни между днешната дата и създадената дата.
# o	Форматира текущата дата във формат "ден/месец/година".

# Импортираме модула datetime, който предоставя функции за работа с дати и часове
import datetime

# Взимаме текущата дата и час чрез метода now() от класа datetime
datetime_now = datetime.datetime.now()

# Създаваме обект от тип datetime за конкретна дата – 25 декември 2024 г.
# Този обект съдържа дата и време (по подразбиране – 00:00:00, ако не е зададено)
christmas_day: datetime = datetime.datetime(2024, 12, 25)

# Изчисляваме разликата между текущата дата и зададената дата.
# Това връща обект от тип timedelta, който съдържа разлика в дни и секунди
time_difference = datetime_now - christmas_day

# Форматираме текущата дата във вида "ден/месец/година" чрез strftime
# %d – ден, %m – месец, %Y – пълна година (напр. 2025)
format_date_today = datetime_now.strftime("%d/%m/%Y")

# Извеждаме текущата дата и час
print(f"Текущата дата и час са {datetime_now}")

# Извеждаме типа на обекта christmas_day и неговата стойност като низ
print(f"Обект от тип {type(christmas_day)} и стойност {str(christmas_day)}")

# Извеждаме разликата между двете дати – ще получим положителна или отрицателна стойност
# в зависимост от това дали 25.12.2024 е в миналото или бъдещето спрямо днес
print(f"Разликата между 25.12.2024 и днес е: {time_difference}")

# Извеждаме форматираната дата
print(f"Днешната дата във формат ден, месец, година: {format_date_today}")

