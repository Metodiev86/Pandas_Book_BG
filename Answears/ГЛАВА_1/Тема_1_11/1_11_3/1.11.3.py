# Напишете програма, която използва модула os за да:
# o	Изведе текущата работна директория.
# o	Създаде нова директория с име "temp_dir".
# o	Провери дали създадената директория съществува.
# o	Премахне създадената директория.

# Импортиране на модула os, който позволява работа с операционната система:
# - файлове, директории, пътища, текуща директория и др.
import os

# Функция, която проверява дали съществува директория с име "temp_dir"
def is_exist_temp_dir() -> bool:
    # os.path.exists проверява дали пътят съществува – може да е файл или директория
    return os.path.exists("temp_dir")

# Функция за създаване на директория "temp_dir", ако не съществува
def create_temp_dir():
    if not is_exist_temp_dir():
        os.mkdir("temp_dir")  # Създаване на директория
        print("Създадена е директорията temp_dir.")

# Функция, която проверява дали "temp_dir" е директория (а не файл например)
def is_directory_temp_dir():
    if is_exist_temp_dir():
        # os.path.isdir проверява дали подаденият път е директория
        print(f"Съществува ли директорията temp_dir: {os.path.isdir('temp_dir')}")
    else:
        print("Няма такава директория!")

# Функция, която премахва директорията "temp_dir", ако съществува
def delete_temp_directory():
    if is_exist_temp_dir():
        # os.rmdir изтрива празна директория
        os.rmdir("temp_dir")
        print("Директорията temp_dir беше изтрита.")

# -------------------------------
# Демонстрация на използването:
# -------------------------------

# Извеждаме текущата работна директория
# Това е директорията, в която се изпълнява програмата
print(f"Текущата директория е: {os.getcwd()}")

# Създаваме директория temp_dir
create_temp_dir()

# Проверяваме дали съществува и дали е директория
is_directory_temp_dir()

# Изтриваме директорията temp_dir
delete_temp_directory()


# os.rmdir("temp_dir") работи само ако директорията е празна. Ако съдържа файлове, ще получиш грешка.
#
# За изтриване на директория с нейното съдържание използвай shutil.rmtree("temp_dir") (трябва да импортираш модула shutil).
