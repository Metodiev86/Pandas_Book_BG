#Напишете програма, която използва модула math за изчисляване на косинус от 60 градуса. Изведете резултата. (Не забравяйте да преобразувате градусите в радиани).

# Импортиране на модула math, който съдържа математически функции,
# включително такива за тригонометрия, преобразуване и др.
import math

# Задаваме ъгъл в градуси
degrees = 60

# Преобразуваме градусите в радиани, тъй като тригонометричните функции
# в Python очакват аргументи в радиани, а не в градуси.
# Формулата е: радиани = градуси × (π / 180)
radians = math.radians(degrees)  # math.radians извършва това преобразуване автоматично

# Изчисляваме косинуса на ъгъла в радиани
# math.cos очаква стойност в радиани
cosine_value = math.cos(radians)

# Извеждаме резултата на екрана във формат:
# "Косинус от 60° е: 0.5"
print(f"Косинус от {degrees}° е: {cosine_value}")

