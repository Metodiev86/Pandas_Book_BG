#Напишете програма, която използва os.listdir() за да изведе всички файлове и директории в текущата директория. Филтрирайте и изведете само файловете.

# Импортиране на модула os, който позволява работа с файловата система
import os

def list_files_in_current_directory():
    # Вземаме текущата работна директория
    current_dir = os.getcwd()

    # Получаваме списък с всички елементи (файлове и директории) в текущата директория
    all_items = os.listdir(current_dir)

    # Извеждаме всички намерени елементи
    print("Всички файлове и директории:")
    for item in all_items:
        print(f" - {item}")  # Показваме всеки елемент поотделно

    # Извеждаме само тези елементи, които са файлове (без директориите)
    print("\nСамо файлове:")
    for item in all_items:
        # os.path.isfile() проверява дали даденият път е към файл
        if os.path.isfile(os.path.join(current_dir, item)):
            print(f" - {item}")

# Извикване на функцията за демонстрация
list_files_in_current_directory()


