#Напишете програма, която използва os.environ за да изведе стойността на системната променлива PATH.

# Импортиране на модула os, който предоставя интерфейс към функционалността на операционната система
import os

# Използваме os.environ.get("PATH"), за да вземем стойността на системната променлива PATH.
# Тази променлива съдържа списък с директории, в които операционната система търси изпълними файлове.
path_value = os.environ.get("PATH")

# Отпечатваме стойността на променливата PATH на конзолата.
# Това обикновено е дълъг низ с пътища, разделени със ; (на Windows) или : (на Linux/Mac).
print(path_value)


