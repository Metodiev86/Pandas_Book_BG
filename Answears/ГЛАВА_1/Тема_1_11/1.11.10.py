#Напишете програма, която използва os.path.join() за да създаде пълен път до файл с име "data.txt" в директория "my_project", независимо от операционната система.

# Импортиране на модула os, който предоставя методи за работа с файловата система
import os


def build_file_path():
    # Директорията, в която ще търсим файла
    folder = "my_project"

    # Името на файла
    filename = "data.txt"

    # Използваме os.path.join() за да създадем пълен път към файла
    # Това ще създаде правилен път към файла, независимо от операционната система (Windows, Linux, macOS)
    full_path = os.path.join(folder, filename)

    # Извеждаме пълния път до файла
    print(f"Пълният път до файла е: {full_path}")

    # Връщаме пълния път
    return full_path


# Извикваме функцията за да проверим резултата
build_file_path()

