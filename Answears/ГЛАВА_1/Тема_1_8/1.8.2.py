#Дефинирайте клас GSM (мобилен телефон) с атрибути model (модел - низ), manufacturer (производител - низ) и has_camera (има ли камера - булев тип). Добавете следните методи: display_info(): Метод, който извежда на конзолата информация за GSM-а във формат: "Модел: [модел], Производител: [производител], Камера: [Да/Не]"; .take_photo(): Метод, който проверява дали телефонът има камера. Ако има, извежда съобщение "Снимката е направена.", в противен случай извежда "Телефонът няма камера и не може да направи снимка.". Създайте два различни обекта от класа GSM с различни модели, производители и информация за камера. Извикайте метода display_info() за всеки обект, за да покажете информацията им. След това извикайте метода take_photo() за всеки обект, за да демонстрирате функционалността му.

class GSM:
    def __init__(self, model: str, manufacturer: str, has_camera: bool):
        # Инициализираме атрибутите на класа
        self.model = model
        self.manufacturer = manufacturer
        self.has_camera = has_camera

    def display_info(self):
        """Метод, който извежда информация за GSM-а"""
        camera_status = "Да" if self.has_camera else "Не"  # Проверяваме дали има камера
        print(f"Модел: {self.model}, Производител: {self.manufacturer}, Камера: {camera_status}")

    def take_photo(self):
        """Метод, който проверява дали има камера и извежда съобщение"""
        if self.has_camera:
            print("Снимката е направена.")
        else:
            print("Телефонът няма камера и не може да направи снимка.")

# Създаваме два различни обекта от класа GSM
gsm1 = GSM("Magic 5 lite", "Honor", True)  # GSM с камера
gsm2 = GSM("3310", "Nokia", False)         # GSM без камера

# Извикваме методите за всеки обект
gsm1.display_info()  # Извеждаме информация за gsm1
gsm1.take_photo()    # Проверяваме дали gsm1 може да направи снимка

print("-" * 80)  # Разделител

gsm2.display_info()  # Извеждаме информация за gsm2
gsm2.take_photo()    # Проверяваме дали gsm2 може да направи снимка


