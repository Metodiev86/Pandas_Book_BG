#Дефинирайте клас Rectangle (правоъгълник) с атрибути width (ширина) и height (височина). Добавете метод area(), който изчислява и връща лицето на правоъгълника. Създайте обект от класа Rectangle и изведете неговото лице.

# Дефинираме клас Rectangle (правоъгълник)
class Rectangle:
    def __init__(self):
        # Инициализираме атрибутите на класа (ширина и височина)
        self.height = 0  # височина по подразбиране
        self.width = 0   # ширина по подразбиране (бяхте написали 'widht', което е грешно)

    # Метод, който изчислява лицето на правоъгълника
    def area(self):
        return self.height * self.width  # Лицето е височината, умножена по ширината

# Създаваме обект от класа Rectangle
my_rectangle = Rectangle()

# Присвояваме стойности на атрибутите на обекта
my_rectangle.height = 5
my_rectangle.width = 10

# Извеждаме стойностите на височината, ширината и лицето на правоъгълника
print(f"Височината на правоъгълника е: {my_rectangle.height}")
print(f"Ширината на правоъгълника е: {my_rectangle.width}")
print(f"Лицето на правоъгълника е: {my_rectangle.area()}")
