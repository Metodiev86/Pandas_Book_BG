#Създайте двумерен NumPy array от списъка от списъци [[1, 2, 3], [4, 5, 6], [7, 8, 9]]. Изведете неговата форма.

# Импортиране на модула numpy
import numpy as np

# Създаване на списък от списъци (двумерен списък)
list_of_lists = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# Преобразуване на списъка от списъци в двумерен NumPy масив
lists_array = np.array(list_of_lists)

# Извеждаме формата (shape) на двумерния масив
print(f"Форамта на {lists_array} e: {lists_array.shape}")
