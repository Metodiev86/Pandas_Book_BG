#4.	Създайте NumPy array c = np.array([[10, 20, 30], [40, 50, 60]]). Умножете всеки елемент на масива по скаларната стойност 5 и изведете резултата.

import numpy as np

# Създаване на двумерен масив
c = np.array([[10, 20, 30], [40, 50, 60]])

# Скалярна стойност
scalar = 5

# Умножаване на всеки елемент от масива по скаларната стойност
print(f"c * scalar = {c * scalar}")
