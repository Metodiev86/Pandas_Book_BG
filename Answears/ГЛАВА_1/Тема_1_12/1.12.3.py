#Създайте два NumPy array-я: a = np.array([1, 2, 3]) и b = np.array([4, 5, 6]). Извършете следните поелементни операции и изведете резултатите:
# o	Събиране (a + b)
# o	Умножение (a * b)
# o	Сравнение (a > b)

# Импортиране на модула numpy
import numpy as np

# Създаване на два NumPy масива
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# Събиране на елементите поелементно
print(f"a + b = {a + b}")  # Ще изведе: [5 7 9]

# Умножение на елементите поелементно
print(f"a * b = {a * b}")  # Ще изведе: [4 10 18]

# Сравнение на елементите поелементно
print(f"a > b = {a > b}")  # Ще изведе: [False False False]
