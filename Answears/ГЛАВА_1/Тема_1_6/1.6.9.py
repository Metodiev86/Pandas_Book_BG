#Напишете програма, която пита потребителя за оценка (A, B, C, D, F). Изведете съответното описание ("Отличен", "Много добър", "Добър", "Среден", "Слаб"). Ако е въведена невалидна оценка, изведете съобщение за грешка.

# Потребителят въвежда оценка
your_ratting = input("Въведете вашата оценка (A, B, C, D, F): ")

# Създаваме речник за съответствието между оценките и техните описания
dict_ratting = {
    "A": "Отличен",
    "B": "Много добър",
    "C": "Добър",
    "D": "Среден",
    "F": "Слаб"
}

# Преобразуваме въведената оценка в главни букви за по-лесно сравнение
ratting_upper = your_ratting.upper()

# Проверяваме дали въведената оценка е валидна (съществува в речника)
if ratting_upper in dict_ratting:
    # Ако оценката е валидна, извеждаме съответното описание
    print(f"Вашата оценка е {dict_ratting[ratting_upper]}")
else:
    # Ако оценката не е валидна, извеждаме съобщение за грешка
    print("Грешно въведена оценка!!!")
