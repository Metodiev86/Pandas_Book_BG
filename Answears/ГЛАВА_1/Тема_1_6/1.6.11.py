#Напишете програма, която обхожда списък с числа. Ако числото е отрицателно, го пропуска и продължава със следващото. Изведете само положителните числа. Използвайте for цикъл и continue.

import random

# Генерираме списък с 10 случайни числа между -100 и 100
list_number = [random.randint(-100, 100) for _ in range(10)]

# Списък за съхранение на положителните числа
positive_numbers = []

# Обхождаме всяко число в списъка
for number in list_number:
    # Ако числото е отрицателно, пропускаме го
    if number > 0:
        positive_numbers.append(number)

# Извеждаме всички числа и положителните числа
print(f"Всички числа: {list_number}")
print(f"Позитивните числа: {positive_numbers}")
