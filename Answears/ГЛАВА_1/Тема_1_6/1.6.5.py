#Напишете код, който проверява дали дадено число е положително, отрицателно или нула.

# Приемане на вход от потребителя
n = input("Въведете произволно число: ")

# Опитваме се да го преобразуваме към десетично число
try:
    number = float(n)  # Еднократна конверсия към float

    # Проверка дали числото е положително, отрицателно или нула
    if number > 0:
        result = "положително"
    elif number < 0:
        result = "отрицателно"
    else:
        result = "нула"

    # Извеждаме резултата
    print(f"Вашето число е {result}.")
except ValueError:
    print("Моля, въведете валидно число.")
