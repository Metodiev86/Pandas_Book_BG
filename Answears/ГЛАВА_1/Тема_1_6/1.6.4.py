#Напишете програма, която обхожда списък с имена и извежда всяко име с неговия пореден номер.

# Списък с имена
list_name = ["Стоян", "Петър", "Валя", "Миша", "Анна", "Стела"]

# Обхождаме списъка, като получаваме индекса (започва от 0) и самото име
for index, name in enumerate(list_name):
    # Отпечатваме индекса + 1, за да започваме броенето от 1
    print(f"{index + 1}. {name}")
