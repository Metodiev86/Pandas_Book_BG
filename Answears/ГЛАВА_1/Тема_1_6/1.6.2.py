#Напишете програма, която изчислява сумата на всички числа от 1 до 100, използвайки while цикъл.

# Инициализираме променливите
num = 0
result = 0

# Използваме while цикъл за изчисляване на сумата
while num < 101:
    result += num  # Добавяме текущото число към резултата
    num += 1  # Увеличаваме числото с 1

# Извеждаме крайния резултат
print(f"Сумата на числата от 1 до 100 е: {result}")


#Също така, можеш да обмислиш да използваш вградената формула за сума на аритметична прогресия, която да ускори изчисленията:
# Формула за сума на числата от 1 до 100
n = 100
sum_of_numbers = n * (n + 1) // 2
print(f"Сумата на числата от 1 до 100 е: {sum_of_numbers}")
