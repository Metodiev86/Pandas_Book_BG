#2.	Напишете функция, която приема цяло число като аргумент. Ако числото е по-малко от 0, предизвикайте изключение ValueError с подходящо съобщение. В противен случай върнете квадрата на числото. Извикайте тази функция в try-except блок и обработете евентуалното изключение.

# Дефинираме функция, която приема цяло число.
# Ако числото е по-малко от 0, предизвикваме ValueError.
# Ако е валидно и положително, връщаме квадрата му.

def square_number(n):
    try:
        m = int(n)  # Опитваме се да конвертираме аргумента в цяло число
    except ValueError:
        raise ValueError("Аргументът не може да се преобразува в цяло число!")  # Генерираме изключение, ако конвертирането не успее

    if m < 0:
        raise ValueError("Числото трябва да бъде положително или 0!")  # Генерираме изключение, ако числото е отрицателно

    return m ** 2  # Връщаме квадрата на числото, ако е валидно

# Тестове:
test_values = [5, -10, "k"]  # Списък с тестови стойности

for value in test_values:
    try:
        result = square_number(value)
        print(f"Квадратът на {value} е: {result}")
    except ValueError as e:
        print(f"ГРЕШКА при стойност {value}: {e}")
