#Напишете програма, която приема две числа от потребителя и ги дели. Използвайте try-except блок, за да обработите грешката ZeroDivisionError, ако потребителят въведе 0 като знаменател.

# Програмата приема две числа от потребителя и ги дели.
# Ако второто число (делителят) е 0, се обработва грешката чрез try-except блок.

# Въвеждане на първото число от потребителя (делимо)
a = float(input("Въведете първото число: "))  # input() връща стринг, затова го преобразуваме към float

# Въвеждане на второто число от потребителя (делител)
b = float(input("Въведете второто число: "))  # отново използваме float за числа с десетична запетая

# Опитваме се да извършим деление, като използваме блок try
try:
    result = a / b  # тук има риск от деление на нула, което ще вдигне изключение ZeroDivisionError
    print(f"Резултатът от делението е: {result}")  # ако всичко е наред, отпечатваме резултата

# Обработка на конкретна грешка: деление на нула
except ZeroDivisionError:
    print("ГРЕШКА: Делителят не може да бъде 0!")  # съобщение при грешка