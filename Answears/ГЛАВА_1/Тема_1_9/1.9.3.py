# Дефинираме функция, която приема потребителски вход
# Опитваме се да го преобразуваме към цяло число с int()
# Ако възникне ValueError (например при вход като "abc"), го обработваме
# Ако преобразуването е успешно, извеждаме числото и съобщение за успех

def user_input_int():
    try:
        n = int(input("Въведете цяло число: "))  # Опит за преобразуване към int
    except ValueError:
        print("ГРЕШКА: Това не е валидно цяло число!")  # Съобщение при грешка
    else:
        print(f"Успешно преобразуване! Въведеното число е: {n}")  # Изпълнява се само ако няма грешка

# Извикваме функцията
user_input_int()