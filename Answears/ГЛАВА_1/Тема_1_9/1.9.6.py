#6.	Напишете програма, която демонстрира използването на try, except и finally блокове. В try блока опитайте да разделите 10 на 0. В except блока изведете съобщение за грешка. Във finally блока изведете съобщение "Край на обработката.".

# Демонстрация на използване на try, except и finally блокове

try:
    # Опитваме да разделим 10 на 0 — това ще предизвика ZeroDivisionError
    result = 10 / 0
    print(result)  # Тази линия никога няма да се изпълни
except ZeroDivisionError:
    # Блокът се изпълнява само ако е възникнала грешка при деление на нула
    print("ГРЕШКА: На нула не се дели!")
finally:
    # Блокът finally се изпълнява винаги, независимо дали е имало грешка или не
    print("Край на програмата!")
