#Напишете програма, която приема три думи от потребителя и ги извежда на един ред, разделени с тире.

# Приемаме три думи от потребителя
first_word = input("Въведете първата дума: ")
second_word = input("Въведете втората дума: ")
third_word = input("Въведете третата дума: ")

# Извеждаме думите на един ред, разделени с тире
print(first_word, second_word, third_word, sep=" - ")
