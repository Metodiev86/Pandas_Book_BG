#Напишете програма, която пита потребителя за две числа. Преобразувайте ги към float и изведете техния среден
# аритметичен.

# Питаме потребителя да въведе две числа
a = float(input("Моля, въведете първото число (a): "))
b = float(input("Моля, въведете второто число (b): "))

# Изчисляваме средното аритметично
mid = (a + b) / 2

# Извеждаме резултата
print(f"Средноаритметичното на {a} и {b} е: {mid}")
