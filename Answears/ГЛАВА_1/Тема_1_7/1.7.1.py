#Напишете функция, която приема две числа като аргументи и връща тяхната сума.

def sum_two_numbers(a, b):  # Дефинираме функцията със два параметъра: a и b
    return a + b  # Връщаме тяхната сума

print(sum_two_numbers(5, 2))  # Извикваме функцията с числа 5 и 2 и отпечатваме резултата
print(sum_two_numbers(10, 20))  # Извежда 30
print(sum_two_numbers(-5, 8))   # Извежда 3