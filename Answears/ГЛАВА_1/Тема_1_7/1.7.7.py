#Напишете функция, която приема речник като аргумент и извежда всички ключове и стойности в него.

def print_dict_content(my_dict: dict):
    for key, value in my_dict.items():
        print(f"Ключ -> {key} : Стойност -> {value}")

# Примерен речник
my_dict = {"A": "Отличен", "B": "Мн. Добър", "C": "Добър", "D": "Среден", "F": "Слаб"}

# Извеждаме съдържанието на речника
print_dict_content(my_dict)
