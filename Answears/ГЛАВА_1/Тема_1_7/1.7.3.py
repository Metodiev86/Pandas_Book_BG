#Напишете функция, която приема списък от числа като аргумент и връща най-голямото число в списъка.

def max_number(numbers: list) -> float:  # Дефинираме функцията, която приема списък от числа
    return max(numbers)  # Връща най-голямото число от списъка

print(max_number([1, 10, 5, 4, 6, 11, 3]))  # Извикваме функцията и извеждаме резултата
print(max_number([2, 8, 6, 1, 3]))  # Извежда 8
print(max_number([-5, -2, -10, -1]))  # Извежда -1
print(max_number([100, 200, 300]))  # Извежда 300
