#Напишете функция, която приема произволен брой низове като аргументи и ги обединява в един низ, разделени с интервали.

def join_string(*args):
    return " ".join(args)  # Обединява всички подадени низове с интервали

# Тест с няколко низови аргумента
print(join_string("Аз", "съм", "Стоян", "Методиев"))
