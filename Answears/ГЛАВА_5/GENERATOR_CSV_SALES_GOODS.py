# Примерен генератор на данни за дневни продажби на продукти, които отговарят на описанието ти. Ще симулираме продажбите за 1 година (365 дни), като:
#
# Редовете ще представляват дати (от 2024-01-01 до 2024-12-31),
#
# Всяка колона ще представлява продукт (например: „Хляб“, „Мляко“, „Яйца“, „Кафе“, „Захар“),
#
# Продажбите ще са цели числа (брой продадени бройки), генерирани на случаен принцип.

import pandas as pd
import numpy as np

# Създаваме списък с дати за 2024 година
dates = pd.date_range(start='2024-01-01', end='2024-12-31', freq='D')

# Списък с продукти
products = ['Хляб', 'Мляко', 'Яйца', 'Кафе', 'Захар']

# Генерираме случaйни продажби за всеки продукт
np.random.seed(42)  # За повторяемост на резултата
data = {product: np.random.randint(0, 100, size=len(dates)) for product in products}

# Създаваме DataFrame и задаваме датите като индекс
df_sales = pd.DataFrame(data, index=dates)
df_sales.index.name = 'Дата'

# Записваме във файл
df_sales.to_csv('sales_data_2024.csv', encoding='utf-8-sig')

