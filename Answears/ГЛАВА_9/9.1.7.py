# 7.	Групирайте df_employees по 'Отдел' и за всяка група приложете функция, която връща DataFrame само с жените от този отдел.

import pandas as pd

# Изходни данни
data = {
    'Отдел': ['Продажби', 'Маркетинг', 'Продажби', 'Развитие', 'Маркетинг', 'Развитие', 'Продажби', 'Маркетинг'],
    'Служител': ['Алиса', 'Борис', 'Цветан', 'Диана', 'Елена', 'Георги', 'Иван', 'Жана'],
    'Пол': ['Ж', 'М', 'М', 'Ж', 'Ж', 'М', 'М', 'Ж'],
    'Години': [25, 32, 40, 28, 35, 45, 30, 26],
    'Заплащане': [5000, 6000, 7500, 8000, 6500, 9000, 7000, 5500],
    'Бонус': [500, None, 750, 1000, 650, 1200, 700, None]
}
df_employees = pd.DataFrame(data)

# Функция за филтриране на жените в даден отдел
def filter_women(group):
    return group[group['Пол'] == 'Ж']

# Групираме по 'Отдел' и прилагаме функцията
result = df_employees.groupby('Отдел').apply(filter_women)

# Показваме резултата
print("\nСлужителите жени във всеки отдел:\n", result)

# Обяснение:
# Функция filter_women(group):
#
# Тази функция филтрира групата (отдела) и връща само редовете, където стойността в колоната 'Пол' е 'Ж' (жените).
#
# Метод .groupby('Отдел'):
#
# Групираме DataFrame-а по колоната 'Отдел', за да създадем отделни групи за всеки отдел.
#
# Метод .apply(filter_women):
#
# Прилагаме функцията filter_women() за всяка група, получена при групирането.
#
# Очакван резултат:
# Този код ще върне DataFrame, съдържащ само жените за всеки отдел. Всеки отдел ще бъде представен с редовете, които отговарят на условието за пол 'Ж'.