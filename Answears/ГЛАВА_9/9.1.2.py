# 2.	Групирайте df_employees по колона 'Пол'.
# а) Намерете средното заплащане за мъжете и жените.
# б) Изведете броя на мъжете и жените във всеки отдел. (Подсказка: може да се наложи да групирате по повече от една колона и след това да използвате .size() или .count())

import pandas as pd

# Изходни данни
data = {
    'Отдел': ['Продажби', 'Маркетинг', 'Продажби', 'Развитие', 'Маркетинг', 'Развитие', 'Продажби', 'Маркетинг'],
    'Служител': ['Алиса', 'Борис', 'Цветан', 'Диана', 'Елена', 'Георги', 'Иван', 'Жана'],
    'Пол': ['Ж', 'М', 'М', 'Ж', 'Ж', 'М', 'М', 'Ж'],
    'Години': [25, 32, 40, 28, 35, 45, 30, 26],
    'Заплащане': [5000, 6000, 7500, 8000, 6500, 9000, 7000, 5500],
    'Бонус': [500, None, 750, 1000, 650, 1200, 700, None]
}

df_employees = pd.DataFrame(data)

# а) Средно заплащане по пол
avg_salary_by_gender = df_employees.groupby('Пол')['Заплащане'].mean()
print("а) Средно заплащане по пол:\n", avg_salary_by_gender)

# б) Брой мъже и жени във всеки отдел
gender_count_by_department = df_employees.groupby(['Отдел', 'Пол']).size().unstack(fill_value=0)
print("\nб) Брой мъже и жени по отдел:\n", gender_count_by_department)

# Обяснения в коментарите:
# а) groupby('Пол')['Заплащане'].mean() връща средната заплата за всеки пол.
#
# б) groupby(['Отдел', 'Пол']).size() брои по групи; .unstack() прави таблицата по-четима (pivot-стил), а fill_value=0 запълва липсващите комбинации с 0.
