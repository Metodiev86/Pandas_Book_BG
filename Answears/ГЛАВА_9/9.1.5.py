# 5.	Използвайте .filter() за да запазите само отделите, в които има повече от 2 служители.

import pandas as pd

# Изходни данни
data = {
    'Отдел': ['Продажби', 'Маркетинг', 'Продажби', 'Развитие', 'Маркетинг', 'Развитие', 'Продажби', 'Маркетинг'],
    'Служител': ['Алиса', 'Борис', 'Цветан', 'Диана', 'Елена', 'Георги', 'Иван', 'Жана'],
    'Пол': ['Ж', 'М', 'М', 'Ж', 'Ж', 'М', 'М', 'Ж'],
    'Години': [25, 32, 40, 28, 35, 45, 30, 26],
    'Заплащане': [5000, 6000, 7500, 8000, 6500, 9000, 7000, 5500],
    'Бонус': [500, None, 750, 1000, 650, 1200, 700, None]
}
df_employees = pd.DataFrame(data)

# Групираме по 'Отдел' и филтрираме, като запазваме само отделите с повече от 2 служители
filtered_departments = df_employees.groupby('Отдел').filter(lambda x: len(x) > 2)

# Показваме резултата
print("\nОтдели с повече от 2 служители:\n", filtered_departments)


# Обяснение:
# .groupby('Отдел') групира DataFrame-а по колоната "Отдел".
#
# .filter(lambda x: len(x) > 2) прилага функция, която проверява дали броят на редовете за всяка група (отдел) е по-голям от 2. Ако условието е изпълнено, групата (отдел) се запазва.
#
# Резултатът е нов DataFrame, съдържащ само тези отдели, в които има повече от 2 служители.
#
# Методът .filter() е много полезен, когато искате да изберете групи от данни, които отговарят на специфични условия, без да променяте структурата на основния DataFrame.