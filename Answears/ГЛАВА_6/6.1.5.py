#Премахнете редовете от df_students, които имат липсващи стойности само в колоните 'Възраст' и 'Оценка', използвайки параметъраsubset.

import pandas as pd
import numpy as np

data = {'Име': ['Алиса', 'Боб', 'Чарли', 'Дейвид', 'Ева'],
        'Възраст': [25, np.nan, 22, 35, np.nan],
        'Оценка': [8.5, 9.2, np.nan, 9.5, 8.9],
        'Град': ['София', None, 'София', 'Варна', 'Бургас']}
df_students = pd.DataFrame(data)

print("Оригинален DataFrame 'df_students':")
print(df_students)
print("-" * 30)

# Премахване на редове, които имат липсващи стойности само в 'Възраст' и 'Оценка'
# Параметърът 'subset' указва кои колони да бъдат проверени за NaN.
# 'how='any'' (по подразбиране) премахва реда, ако има NaN в която и да е от посочените колони.
df_filtered_subset = df_students.dropna(subset=['Възраст', 'Оценка'])

print("\nDataFrame след премахване на редове с NaN само в 'Възраст' и 'Оценка':")
print(df_filtered_subset)