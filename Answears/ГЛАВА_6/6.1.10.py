#10.	За колоната 'Град', запълнете липсващите стойности с най-често срещания град (модата).

import pandas as pd
import numpy as np

data = {'Име': ['Алиса', 'Боб', 'Чарли', 'Дейвид', 'Ева'],
        'Възраст': [25, np.nan, 22, 35, np.nan],
        'Оценка': [8.5, 9.2, np.nan, 9.5, 8.9],
        'Град': ['София', None, 'София', 'Варна', 'Бургас']}
df_students = pd.DataFrame(data)

print("Оригинален DataFrame 'df_students':")
print(df_students)
print("-" * 30)

# 1. Намиране на модата (най-често срещания град) в колоната 'Град'
# Методът .mode() връща Series, дори ако има само една мода.
# Затова взимаме първия елемент от резултата [0], за да получим самата стойност.
most_frequent_city = df_students['Град'].mode()[0]
print(f"Най-често срещан град: {most_frequent_city}")

# 2. Запълване на липсващите стойности в колоната 'Град' с най-често срещания град
df_students['Град'] = df_students['Град'].fillna(most_frequent_city)

print("\nDataFrame след запълване на липсващите стойности в 'Град' с модата:")
print(df_students)