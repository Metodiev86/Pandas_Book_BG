#6.	Запълнете липсващите стойности в колоната 'Възраст' със средната възраст.

import pandas as pd
import numpy as np

data = {'Име': ['Алиса', 'Боб', 'Чарли', 'Дейвид', 'Ева'],
        'Възраст': [25, np.nan, 22, 35, np.nan],
        'Оценка': [8.5, 9.2, np.nan, 9.5, 8.9],
        'Град': ['София', None, 'София', 'Варна', 'Бургас']}
df_students = pd.DataFrame(data)

print("Оригинален DataFrame 'df_students':")
print(df_students)
print("-" * 30)

# Изчисляване на средната възраст, игнорирайки NaN стойностите
# Методът .mean() автоматично игнорира NaN стойностите по подразбиране.
mean_age = df_students['Възраст'].mean()
print(f"Средна възраст (изчислена без липсващите стойности): {mean_age:.2f}")

# Запълване на липсващите стойности в колоната 'Възраст' със средната възраст
# Използваме .fillna() върху конкретната колона.
df_students['Възраст'] = df_students['Възраст'].fillna(mean_age)

print("\nDataFrame след запълване на липсващите стойности във 'Възраст' със средната:")
print(df_students)