# 4.	Създайте нов DataFrame, който съдържа само учениците, за които има информация както за възрастта, така и за оценката.

import pandas as pd
import numpy as np

data = {'Име': ['Алиса', 'Боб', 'Чарли', 'Дейвид', 'Ева'],
        'Възраст': [25, np.nan, 22, 35, np.nan],
        'Оценка': [8.5, 9.2, np.nan, 9.5, 8.9],
        'Град': ['София', None, 'София', 'Варна', 'Бургас']}
df_students = pd.DataFrame(data)

print("Оригинален DataFrame 'df_students':")
print(df_students)
print("-" * 30)

# Създаване на нов DataFrame, съдържащ само ученици с информация за Възраст И Оценка
# Използваме 'subset' аргумента, за да укажем кои колони да бъдат проверени за липсващи стойности.
# 'how='all'' би премахнал ред само ако ВСИЧКИ стойности в 'subset' са NaN.
# Тъй като искаме редът да бъде премахнат, ако ИМА липсваща стойност в някоя от тези колони,
# използваме 'how='any'' (което е и по подразбиране).
df_students_complete_info = df_students.dropna(subset=['Възраст', 'Оценка'])

print("\nDataFrame с ученици, за които има информация и за Възраст, и за Оценка:")
print(df_students_complete_info)