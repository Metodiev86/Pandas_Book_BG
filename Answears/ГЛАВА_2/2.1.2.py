# Изведете имената на колоните и формата на DataFrame-а.

import pandas as pd  # Импортиране на библиотеката pandas

# Създаване на списък от речници – всеки речник представя един продукт с неговите характеристики
data = [
    {'Продукт': 'Лаптоп', 'Цена': 1200.50, 'Наличност': 10},
    {'Продукт': 'Мишка', 'Цена': 25.99, 'Наличност': 50},
    {'Продукт': 'Клавиатура', 'Цена': 75.00, 'Наличност': 25}
]

# Създаване на DataFrame от списъка от речници
df = pd.DataFrame(data)

# Отпечатване на съдържанието на DataFrame-а
print("Създаденият DataFrame:\n")
print(df)

# Отпечатване на имената на колоните
print("\nИмена на колоните:")
print(df.columns.tolist())  # .tolist() за по-четим изход

# Отпечатване на формата на DataFrame-а (редове, колони)
print(f"\nФорма на DataFrame-а (редове, колони): {df.shape}")
