# 1.	Дадени са два DataFrame-а: df_employees с колони ('ID', 'Име', 'Отдел') и df_salaries с колони ('ID', 'Заплата'). Слейте ги, използвайки колоната 'ID' като ключ.

import pandas as pd

# DataFrame със служители
df_employees = pd.DataFrame({
    'ID': [1, 2, 3],
    'Име': ['Алиса', 'Боб', 'Чарли'],
    'Отдел': ['HR', 'IT', 'Финанси']
})

# DataFrame със заплати
df_salaries = pd.DataFrame({
    'ID': [1, 2, 3],
    'Заплата': [3000, 4000, 3500]
})

# Сливане по общата колона 'ID'
df_merge = pd.merge(df_employees, df_salaries, on='ID')

print(df_merge)

# Полученият DataFrame ще съдържа всички колони от двата източника, обединени по съвпадащи стойности в колоната ID.
# Ако има ID, който липсва в единия от DataFrame-ите, ще се използва how='inner' по подразбиране (т.е. само съвпадащите записи ще бъдат включени).