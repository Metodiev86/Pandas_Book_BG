# 3.	Сравнете два DataFrame-а, като запазите оригиналната форма на резултата (еднаквите стойности да бъдат NaN) и зададете потребителски имена за нивата на MultiIndex колоните.

import pandas as pd

# Създаваме два DataFrame-а с еднаква структура
df1 = pd.DataFrame({
    'Code': [1, 2, 3],
    'Price': [10.0, 20.0, 15.0],
    'Stock': [5, 2, 3]
})

df2 = pd.DataFrame({
    'Code': [1, 2, 3],
    'Price': [10.0, 22.0, 15.0],  # Разлика в Price за ред 2
    'Stock': [5, 3, 3]            # Разлика в Stock за ред 2
})

# Сравняваме двата DataFrame-а и:
# - запазваме оригиналната форма (keep_shape=True)
# - задаваме собствено име за нивата на MultiIndex колоните
result = df1.compare(
    df2,
    keep_shape=True,                   # Формата остава като оригинала
    keep_equal=False,                 # Общите стойности се заместват с NaN
    result_names=("Оригинал", "Промяна")  # Имената на двете нива в колоните
)

print(result)

# Обяснение:
# keep_shape=True: резултатът запазва всички редове и колони от оригиналните DataFrame-и.
#
# keep_equal=False: еднаквите стойности се заместват с NaN, така че лесно се виждат само промените.
#
# result_names: именува двете колони в резултата (MultiIndex) по персонализиран начин – например „Оригинал“ и „Промяна“.
