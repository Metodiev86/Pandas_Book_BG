# 1.	Дадени са два DataFrame-а с еднаква структура, но с някои различаващи се стойности. Използвайте .compare() за да идентифицирате разликите.

import pandas as pd

# Създаване на два DataFrame-а с еднаква структура
df1 = pd.DataFrame({
    'Code': [1, 2, 3],
    'Price': [10.0, 20.0, 15.0],
    'Stock': [5, 2, 3]
})

df2 = pd.DataFrame({
    'Code': [1, 2, 3],
    'Price': [10.0, 22.0, 15.0],  # Разлика на ред 2
    'Stock': [5, 3, 3]            # Разлика на ред 2
})

# Използваме .compare() за да видим разликите
comparison = df1.compare(df2)

# Показваме резултата
print(comparison)

# Коментар:
# .compare() сравнява стойностите между два DataFrame-а със съвпадаща структура.
#
# В резултата се показват само тези клетки, където стойностите се различават, с две колони: 'self' (от df1) и 'other' (от df2).
#
# По подразбиране .compare() работи по позиция, а не по индекси. Ако индексите се различават, първо трябва да ги уеднаквим.