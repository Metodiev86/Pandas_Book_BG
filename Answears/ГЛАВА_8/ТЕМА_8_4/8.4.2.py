# 2.	Сравнете два DataFrame-а и изведете само редовете, където има поне една разлика.

import pandas as pd

# Създаване на два DataFrame-а с еднакъв индекс и колони
df1 = pd.DataFrame({
    'Code': [1, 2, 3],
    'Price': [10.0, 20.0, 15.0],
    'Stock': [5, 2, 3]
})

df2 = pd.DataFrame({
    'Code': [1, 2, 3],
    'Price': [10.0, 22.0, 15.0],  # Разлика при Price за ред 2
    'Stock': [5, 3, 3]            # Разлика при Stock за ред 2
})

# Намиране на редовете, където поне една стойност се различава
rows_with_diff = df1.ne(df2).any(axis=1)
result = df1[rows_with_diff]

# Показваме само различаващите се редове от df1 (може и от df2)
print(result)

# Коментар:
# df1.ne(df2) създава булев DataFrame, където всяка клетка е True, ако стойностите се различават.
#
# .any(axis=1) проверява по редове дали има поне една разлика.
#
# Извеждаме редовете от df1, където има разлика спрямо df2. Ако искаме редовете от df2, заменяме df1[rows_with_diff] с df2[rows_with_diff].