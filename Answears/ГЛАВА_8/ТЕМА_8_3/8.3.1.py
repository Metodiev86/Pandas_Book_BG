# 1.	Дадени са два DataFrame-а, където колоната 'ID' е зададена като индекс и за двата. Съединете ги, използвайки метода .join().

import pandas as pd

# Примерни данни за първия DataFrame
df_left = pd.DataFrame({
    'Name': ['Alice', 'Bob', 'Charlie'],
    'Score': [85, 90, 78]
}, index=[1, 2, 3])  # 'ID' като индекс

# Примерни данни за втория DataFrame
df_right = pd.DataFrame({
    'Grade': ['A', 'A+', 'B+']
}, index=[1, 2, 3])  # 'ID' също като индекс

# Съединяване по индекс чрез .join()
joined_df = df_left.join(df_right)

# Показваме резултата
print(joined_df)

# Коментари:
# Методът .join() по подразбиране съединява по индекса на двата DataFrame-а.
#
# Ако индексите не съвпадат напълно, резултатът ще съдържа NaN за несъвпадащите стойности (може да се контролира чрез how='inner', how='outer' и др.).
#
# Методът е кратък и удобен, когато работим с индекс вместо с ключова колона.
