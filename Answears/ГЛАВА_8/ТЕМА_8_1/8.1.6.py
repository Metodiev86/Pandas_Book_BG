# 6.	Дадени са два DataFrame-а с частично съвпадащи индекси. Конкатенирайте ги по колони, използвайки външно съединяване (outer join), така че в резултата да присъстват всички редове от двата DataFrame-а (липсващите стойности да бъдат NaN).

import pandas as pd

# Първи DataFrame с индекс 1 и 2
df1 = pd.DataFrame({
    'Име': ['Алиса', 'Боб'],
    'Възраст': [25, 30]
}, index=[1, 2])

# Втори DataFrame с индекс 2 и 3
df2 = pd.DataFrame({
    'Град': ['София', 'Пловдив'],
    'Държава': ['България', 'България']
}, index=[2, 3])

# Конкатенация по колони с външно съединяване по индекс (outer join)
result = pd.concat([df1, df2], axis=1, join='outer')

print(result)

# Обяснение:
#
# axis=1 → Конкатенацията се извършва по колони (хоризонтално).
#
# join='outer' → Включва всички индекси от двата DataFrame-а.
#
# При липсващи стойности в някой от DataFrame-ите за даден индекс, се попълва NaN.