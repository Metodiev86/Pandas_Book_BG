#4.	Имате два DataFrame-а с еднакви колони, но с потенциално дублиращи се индекси. Конкатенирайте ги по редове, като създадете йерархичен индекс, където първото ниво указва произхода на данните (например, 'DataFrame 1', 'DataFrame 2').

import pandas as pd

# Примерни DataFrame-и
df1 = pd.DataFrame({'Име': ['Алиса', 'Боб'], 'Възраст': [25, 30]})
df2 = pd.DataFrame({'Име': ['Чарли', 'Дейвид'], 'Възраст': [35, 40]})

# Конкатениране на DataFrame-ите по редове с йерархичен индекс
result = pd.concat([df1, df2], keys=['DataFrame 1', 'DataFrame 2'])

print(result)
