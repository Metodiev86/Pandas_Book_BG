#2.	Имате два DataFrame-а с еднакви редове, но различни колони. Напишете код, който ги комбинира по колони.

import pandas as pd

# Примерни DataFrame-и с еднакви редове, но различни колони
df1 = pd.DataFrame({
    'Име': ['Алиса', 'Боб'],
    'Възраст': [25, 30]
})

df2 = pd.DataFrame({
    'Град': ['София', 'Пловдив'],
    'Статус': ['Активен', 'Неактивен']
})

# Комбиниране по колони чрез pd.concat()
df_combo = pd.concat([df1, df2], axis=1)

print(df_combo)
