# 5.	Дадени са два DataFrame-а с частично съвпадащи колони. Конкатенирайте ги по редове, използвайки вътрешно съединяване (inner join), така че в резултата да присъстват само общите колони.

import pandas as pd

# Създаваме два DataFrame-а с частично съвпадащи колони
df1 = pd.DataFrame({
    'Име': ['Алиса', 'Боб'],
    'Възраст': [25, 30],
    'Град': ['София', 'Пловдив']
})

df2 = pd.DataFrame({
    'Име': ['Чарли', 'Дейвид'],
    'Възраст': [35, 40],
    'Държава': ['България', 'България']
})

# Конкатенация по редове с вътрешно съединяване (само общите колони се запазват)
result = pd.concat([df1, df2], join='inner')

print(result)

# Обяснение:
#
# Параметърът join='inner' гарантира, че само колоните, които съществуват и в двата DataFrame-а ще останат в резултата ('Име' и 'Възраст' в случая).
#
# Това е полезно при обединяване на непълни таблици с частично припокриваща се структура.