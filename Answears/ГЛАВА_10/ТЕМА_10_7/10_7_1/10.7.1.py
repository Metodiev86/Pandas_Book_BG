# 1.	Четене на таблица от уебсайт: Изберете уебсайт с поне една HTML таблица (например, таблица с данни за акции, спортни резултати и т.н.). Използвайте pd.read_html() за да прочетете таблицата в DataFrame. Изведете първите няколко реда и информация за DataFrame-а. Ако сайтът има повече от една таблица, опитайте да идентифицирате индекса на желаната таблица.

# ===============================================================================================
#                                                                                               |
# В примера ще изберем статията от www.wikipedia.bg за Световното първенство по футбол 1994г.   |
#                                                                                               |
#================================================================================================

import pandas as pd
import urllib.parse

# Оригинален URL с кирилица
url_bg = "https://bg.wikipedia.org/wiki/Световно_първенство_по_футбол_1994"

# URL-кодиране (ще замени кирилицата със съответните кодове)
url_encoded = urllib.parse.quote(url_bg, safe=':/')

# Прочитане на HTML таблици
tables = pd.read_html(url_encoded)

# Показване на брой таблици и първата таблица
print(f"Намерени таблици: {len(tables)}")

# За илюстрация, избираме първата таблица (index 0)
df = tables[0]

# Показване на първите няколко реда и информация за структурата
print(df.head())
print("\nИнформация за DataFrame:")
print(df.info())

#========================================================Обяснение:======================================================
#
# pd.read_html(url_encoded) прави HTTP заявка и извлича всички <table> елементи от страницата, връщайки списък от DataFrame обекти.
#
# Проверяваме колко таблици има – понякога страницата съдържа няколко таблици (примерно със статистика, резултати и др.).
#
# Избираме нужната таблица по индекс от списъка tables, например първата (tables[0]).
#
# Използваме .head() и .info(), за да разгледаме съдържанието и типове данни на колоните.
#
# Това е прост и ефективен начин за извличане на таблични данни от уеб страница в DataFrame. Ако има повече таблици, можеш да разглеждаш tables[1], tables[2] и т.н., докато намериш тази, която ти трябва.
