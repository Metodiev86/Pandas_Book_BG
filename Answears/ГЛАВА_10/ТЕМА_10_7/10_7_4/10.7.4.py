# 4.	Запис на част от DataFrame в HTML: Създайте DataFrame и изберете подмножество от него (например, няколко колони и редове). Конвертирайте това подмножество в HTML таблица и го запишете във файл subset_table.html. Отворете файла в браузър, за да видите резултата.

import pandas as pd

# Създаваме пълен DataFrame с произволни данни
data = {
    "Име": ["Анна", "Борис", "Виктор", "Галя", "Димитър"],
    "Възраст": [28, 34, 45, 23, 39],
    "Град": ["София", "Пловдив", "Варна", "Русе", "Бургас"],
    "Оценка": [5.6, 6.0, 4.8, 5.9, 5.2]
}
df = pd.DataFrame(data)

# Избираме подмножество: първите 3 реда и 2 колони
subset = df.loc[:2, ["Име", "Оценка"]]

# Записваме подмножеството в HTML файл без индекс
subset.to_html("subset_table.html", index=False)

print("HTML таблицата беше записана в 'subset_table.html'.")
