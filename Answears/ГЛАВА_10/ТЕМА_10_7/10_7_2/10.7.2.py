# 2.	Четене на таблица с атрибути: Намерете уебсайт, където таблицата има специфични HTML атрибути (например, class или id). Използвайте параметъра attrs на pd.read_html() за да прочетете само тази конкретна таблица.

import pandas as pd
import urllib.parse

# Оригинален URL с кирилица
url_bg = "https://bg.wikipedia.org/wiki/Световно_първенство_по_футбол_1994"

# URL-кодиране (ще замени кирилицата със съответните кодове)
url_encoded = urllib.parse.quote(url_bg, safe=':/')


# Прочитаме всички таблици, които имат клас "wikitable sortable"
tables = pd.read_html(url_encoded, attrs={"class": "wikitable"})

# Проверяваме колко таблици сме заредили
print(f"Заредени таблици: {len(tables)}\n")

# Ако има поне една, показваме първата
if tables:
    df = tables[0]
    print("Първа таблица с class='wikitable':")
    print(df.head())           # първите 5 реда
    print("\nИнформация:")
    print(df.info())
else:
    print("Не е намерена таблица с този class.")


