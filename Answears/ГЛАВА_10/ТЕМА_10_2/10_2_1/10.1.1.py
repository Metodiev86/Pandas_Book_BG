# #1.	Прочетете CSV с персонализации: Създайте CSV файл data_custom.csv със следните данни (използвайте запетая като разделител):
# id;name;age;city;date
# 1;Alice;25;Sofia;2023/10/26
# 2;Bob;30;Plovdiv;2023/10/27
# 3;Charlie;22;Varna;2023/10/28
# Прочетете този файл в Pandas DataFrame, като използвате ; като разделител, зададете колоните id като индекс, прочетете колоната date като datetime обект и зададете имената на колоните съответно на 'ID', 'Име', 'Възраст', 'Град', 'Дата'. Изведете DataFrame-а.



import pandas as pd

# Създаваме CSV файл със съдържание и разделител ";"
csv_content = """id;name;age;city;date
1;Alice;25;Sofia;2023/10/26
2;Bob;30;Plovdiv;2023/10/27
3;Charlie;22;Varna;2023/10/28
"""

# Записваме съдържанието във файл
with open('data_custom.csv', 'w', encoding='utf-8') as f:
    f.write(csv_content)

# Прочитаме CSV файла с нужните параметри
df = pd.read_csv(
    'data_custom.csv',
    sep=';',  # използваме ; като разделител
    index_col=['ID'],  # използваме 'id' като индекс (по оригинално име от файла)
    parse_dates=['Дата'],  # новото име на колоната след names=
    names=['ID', 'Име', 'Възраст', 'Град', 'Дата'],  # задаваме нови имена
    header=0  # оригиналният заглавен ред се намира на ред 0
)

# Преглед на резултата
print("DataFrame с персонализирано четене от CSV файл:\n")
print(df)
