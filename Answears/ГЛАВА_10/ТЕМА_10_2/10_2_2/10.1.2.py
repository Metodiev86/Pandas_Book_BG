# 2.	Запишете част от DataFrame в CSV: Създайте DataFrame с произволни данни (поне 3 колони и 5 реда). След това изберете само две от колоните и запишете този подмножество в нов CSV файл subset.csv, като използвате табулация като разделител и не включвате индекса.

import pandas as pd

# Създаваме примерен DataFrame с 3 колони и 5 реда
data = {
    'Име': ['Алиса', 'Боб', 'Чарли', 'Даниел', 'Ева'],
    'Град': ['София', 'Пловдив', 'Варна', 'Бургас', 'Русе'],
    'Години': [25, 30, 22, 35, 28]
}

df = pd.DataFrame(data)

# Извличаме подмножество с 2 колони: 'Име' и 'Години'
subset = df[['Име', 'Години']]

# Записваме подмножество в CSV файл с табулация като разделител и без индекс
subset.to_csv('subset.csv', sep='\t', index=False)

# Потвърждаваме резултата
print("Подмножество успешно записано в 'subset.csv' с разделител табулация.")
