# 4.	Работа с вложен XML (просто): Създайте XML файл books.xml:
# <books>
#   <book title="Книга 1">
#     <author>Автор 1</author>
#   </book>
#   <book title="Книга 2">
#     <author>Автор 2</author>
#   </book>
# </books>
#
#  Прочетете заглавията и авторите в DataFrame, като използвате XPath.

import pandas as pd

# --- 1. Прочитане на XML файл с вложена структура ---
df = pd.read_xml(
    'books.xml',
    xpath='.//book'  # Извлича всеки <book> елемент
)

# --- 2. Преименуване на колоните за по-ясно представяне ---
df = df.rename(columns={'@title': 'Заглавие', 'author': 'Автор'})

# --- 3. Извеждане ---
print(df)

#=============================================Обяснение:=====================================================
#
# xpath='.//book' избира всички елементи <book>, независимо от вложеността.
#
# @title указва, че title е атрибут, а не дъщерен елемент.
#
# author е дъщерен елемент на <book>, така че се извлича автоматично.
