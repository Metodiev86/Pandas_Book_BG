# 3.	Запис в XML с персонализирани имена: Създайте DataFrame с колони 'Име', 'Възраст' и 'Град'. Запишете го в XML файл output.xml с име на корена 'хора' и име на всеки ред 'човек'. Не включвайте индекса.

import pandas as pd

# --- 1. Създаване на DataFrame с произволни данни ---
df = pd.DataFrame({
    'Име': ['Ана', 'Борис', 'Цветелина'],
    'Възраст': [28, 34, 22],
    'Град': ['София', 'Пловдив', 'Варна']
})

# --- 2. Записване в XML файл с персонализирани тагове ---
df.to_xml(
    'output.xml',           # име на изходния файл
    root_name='хора',       # име на кореновия елемент
    row_name='човек',       # име на всеки запис (ред)
    index=False,            # не включвай индекса
    encoding='utf-8'        # задаваме подходящо кодиране
)

# --- 3. Потвърждение ---
print("Файлът 'output.xml' беше успешно създаден.")
