# 1.	Четене от конкретен лист: Създайте Excel файл data.xlsx с два листа: "Лист1" и "Лист2". Въведете произволни данни в двата листа (например, по 3 колони и 5 реда). Прочетете данните само от "Лист2" в Pandas DataFrame и изведете го.

import pandas as pd

# --- 1. Създаване на Excel файл с два листа ---
# Създаваме два различни DataFrame-а с произволни данни
df1 = pd.DataFrame({
    'Име': ['Ана', 'Боби', 'Виктор', 'Галя', 'Дани'],
    'Град': ['София', 'Пловдив', 'Варна', 'Бургас', 'Русе'],
    'Възраст': [23, 34, 29, 41, 25]
})

df2 = pd.DataFrame({
    'Продукт': ['Хляб', 'Мляко', 'Сирене', 'Яйца', 'Кисело мляко'],
    'Цена': [1.20, 2.50, 5.80, 3.10, 1.60],
    'Наличност': [50, 60, 40, 100, 70]
})

# Записваме и двата DataFrame-а в отделни листове в един Excel файл
excel_file = 'data.xlsx'
with pd.ExcelWriter(excel_file) as writer:
    df1.to_excel(writer, sheet_name='Лист1', index=False)
    df2.to_excel(writer, sheet_name='Лист2', index=False)

# --- 2. Четене на данни само от "Лист2" ---
df_l2 = pd.read_excel(excel_file, sheet_name='Лист2')

# --- 3. Преглед на прочетения DataFrame ---
print("Данни от Лист2:\n")
print(df_l2)


#=============================================Обяснение:=========================================
#
# pd.ExcelWriter(...) — използваме го за записване на множество листове в един .xlsx файл.
#
# sheet_name='Лист2' — указваме от кой лист да прочетем.
#
# index=False — не записваме индекса в Excel файла, за по-четим формат.
#
# След изпълнение ще имате файл data.xlsx с два листа, а в терминала ще видите съдържанието на Лист2.