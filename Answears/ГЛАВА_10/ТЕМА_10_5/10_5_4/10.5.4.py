# 4.	Четене на определени колони и задаване на индекс: Създайте Excel файл data_with_id.xlsx с колони 'ID', 'Име', 'Възраст', 'Град'. Прочетете файла, като извлечете само колоните 'Име' и 'Възраст', и зададете колоната 'Име' като индекс на DataFrame-а. Изведете резултата.

import pandas as pd

# --- 1. Създаване на примерни данни и запис в Excel файл ---

# Създаваме DataFrame с колони 'ID', 'Име', 'Възраст', 'Град'
df_original = pd.DataFrame({
    'ID': [1, 2, 3, 4],
    'Име': ['Анна', 'Борис', 'Галя', 'Даниел'],
    'Възраст': [28, 35, 22, 41],
    'Град': ['София', 'Пловдив', 'Варна', 'Бургас']
})

# Записваме данните във файл data_with_id.xlsx
excel_file = 'data_with_id.xlsx'
df_original.to_excel(excel_file, index=False)

# --- 2. Прочитане само на определени колони и задаване на индекс ---

# Прочитаме само колоните 'Име' и 'Възраст' и задаваме 'Име' като индекс
df_filtered = pd.read_excel(
    excel_file,
    usecols=['Име', 'Възраст'],  # избираме само нужните колони
    index_col='Име'              # задаваме 'Име' за индекс
)

# --- 3. Преглед на резултата ---
print("Извлечени данни с 'Име' като индекс:\n")
print(df_filtered)
