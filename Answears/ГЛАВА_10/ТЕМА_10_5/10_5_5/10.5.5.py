# 5.	Запис с контролиране на индекса: Създайте DataFrame с произволни данни и индекс, който не е поредица от числа (например, букви). Запишете DataFrame-а в Excel файл indexed_data.xlsx веднъж с включен индекс и веднъж без индекс (в два различни файла или на два различни листа). Отворете Excel файла и сравнете резултатите.

import pandas as pd

# --- 1. Създаване на DataFrame с индекс от букви ---
df = pd.DataFrame({
    'Продукт': ['Ябълка', 'Банан', 'Круша', 'Праскова'],
    'Количество': [10, 20, 15, 12],
    'Цена': [1.20, 0.80, 1.50, 1.70]
}, index=['A', 'B', 'C', 'D'])  # използваме букви като индекс

# --- 2. Запис в Excel файл с включен индекс ---
df.to_excel('indexed_with_index.xlsx', index=True)

# --- 3. Запис в Excel файл без включен индекс ---
df.to_excel('indexed_without_index.xlsx', index=False)
print("Файловете са записани!")

