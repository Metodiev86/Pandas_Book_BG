# 2.	Четене без заглавен ред и задаване на имена: Създайте Excel файл no_header.xlsx с 3 колони и 5 реда с произволни данни, без ред със заглавия. Прочетете този файл в Pandas DataFrame и задайте имената на колоните като 'Колона A', 'Колона B', 'Колона C'. Изведете DataFrame-а.

import pandas as pd
from openpyxl import Workbook

# --- 1. Създаване на Excel файл без заглавен ред ---

# Данни за записване (без заглавен ред)
data = [
    [10, 20, 30],
    [15, 25, 35],
    [12, 22, 32],
    [18, 28, 38],
    [14, 24, 34]
]

# Създаване на нов Excel файл с openpyxl
wb = Workbook()
ws = wb.active
ws.title = "Данни"

# Записваме само данни, без заглавия
for row in data:
    ws.append(row)

# Запазване във файл
excel_file = "no_header.xlsx"
wb.save(excel_file)

# --- 2. Четене на Excel файла без заглавен ред ---

# Задаваме header=None, за да не се интерпретира първия ред като заглавен
df = pd.read_excel(excel_file, header=None)

# Присвояваме имена на колоните
df.columns = ['Колона A', 'Колона B', 'Колона C']

# --- 3. Извеждане на резултата ---
print("Съдържание на Excel файла без заглавен ред:")
print(df)
