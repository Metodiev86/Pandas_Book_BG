# 2.	Форматиране на различни типове колони: Създайте DataFrame с колони, съдържащи числа с плаваща запетая, цели числа и дати. Експортирайте го в LaTeX, като форматирате числата до два десетични знака и датите във формат ДД.ММ.ГГГГ.

import pandas as pd
import numpy as np

# --- 1. Създаване на DataFrame ---
df = pd.DataFrame({
    'Продукт': ['Продукт A', 'Продукт B', 'Продукт C'],
    'Количество': [10, 25, 30],  # Цели числа
    'Цена (лв.)': [15.6789, 42.5, 8.3],  # Float
    'Дата на продажба': pd.to_datetime(['2024-03-15', '2024-04-01', '2024-05-12'])  # Дата
})

# --- 2. Форматиране ---
df['Цена (лв.)'] = df['Цена (лв.)'].map(lambda x: f"{x:.2f}")  # 2 знака
df['Дата на продажба'] = df['Дата на продажба'].dt.strftime('%d.%m.%Y')  # формат ДД.ММ.ГГГГ

# --- 3. Експортиране в LaTeX ---
latex_code = df.to_latex(
    index=False,
    caption='Форматирана таблица с продажби',
    label='tab:formatted_sales',
    escape=False  # Позволява запазване на кирилица и специални символи като лв.
)

# --- 4. Запис във файл ---
with open('formatted_sales.tex', 'w', encoding='utf-8') as f:
    f.write(latex_code)

print("LaTeX файлът 'formatted_sales.tex' е създаден успешно.")
