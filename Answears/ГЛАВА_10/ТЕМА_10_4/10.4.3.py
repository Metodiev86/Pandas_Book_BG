# 3.	Четене на данни, разделени със запетая: Създайте текстов низ, който представлява таблични данни, разделени със запетая (включително заглавен ред). Копирайте този низ в клипборда. Използвайте pd.read_clipboard() с подходящ параметър, за да прочетете данните в DataFrame. Изведете DataFrame-а.

import pandas as pd

# Създаваме текстов низ с данни, разделени със запетая
csv_text = """Име,Град,Възраст
Анна,София,28
Борис,Пловдив,35
Виктор,Варна,42
Галя,Бургас,31
Даниел,Русе,24
"""

# Копираме текста в клипборда – използва се за симулация на поставяне от Excel или Notepad
pd.Series([csv_text]).to_clipboard(index=False, header=False)

# Прочитаме съдържанието на клипборда в DataFrame, като използваме разделител запетая
df = pd.read_clipboard(sep=',')

# Извеждаме резултата
print("Съдържание на DataFrame от клипборда (CSV с разделител ','):\n")
print(df)
