# 2.	Копиране на подмножество: Създайте DataFrame с поне 4 колони и 5 реда с произволни данни. Изберете подмножество от DataFrame-а (например, първите 3 реда и две от колоните). Копирайте това подмножество в клипборда, без да включвате индекса и използвайки запетая като разделител. След това (симулирайки поставяне в текстов редактор) опишете как ще изглеждат данните в клипборда.

import pandas as pd

# Създаване на примерен DataFrame с произволни данни
data = {
    'Име': ['Анна', 'Борис', 'Виктор', 'Галя', 'Даниел'],
    'Град': ['София', 'Пловдив', 'Варна', 'Бургас', 'Русе'],
    'Възраст': [28, 35, 42, 31, 24],
    'Професия': ['Инженер', 'Лекар', 'Учител', 'Анализатор', 'Програмист']
}
df = pd.DataFrame(data)

# Избор на подмножество: първите 3 реда и две от колоните
subset = df.loc[:2, ['Име', 'Град']]

# Копиране на подмножеството в клипборда:
# - `index=False` – без индекс
# - `sep=','` – използваме запетая като разделител
subset.to_clipboard(index=False, sep=',')

print("Подмножество е копирано в клипборда.")
