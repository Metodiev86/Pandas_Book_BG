# 4.	Копиране с персонализиран разделител: Създайте DataFrame с две числови колони. Копирайте DataFrame-а в клипборда, като използвате знака "|" (вертикална черта) като разделител и включите индекса. Опишете как ще изглеждат данните в клипборда.

import pandas as pd

# Създаваме DataFrame с две числови колони
df = pd.DataFrame({
    'Колона1': [10, 20, 30, 40],
    'Колона2': [5.5, 6.6, 7.7, 8.8]
}, index=['a', 'b', 'c', 'd'])

# Копираме DataFrame-а в клипборда:
# - sep='|' указва персонализиран разделител
# - index=True означава, че ще включим индекса
df.to_clipboard(sep='|', index=True)

# Извеждаме DataFrame-а за потвърждение
print("Копиран DataFrame със знак '|' като разделител и включен индекс:\n")
print(df)


# Как ще изглеждат данните в клипборда (например ако се поставят в Notepad или Excel):
#====================================================================================
#      |Колона1|Колона2
# a    |10     |5.5
# b    |20     |6.6
# c    |30     |7.7
# d    |40     |8.8
#====================================================================================
# Важно: Вертикалната черта | е подходяща за визуално разграничение между колони,
