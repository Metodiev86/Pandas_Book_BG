# o	Използвайте read_sql_query(), за да извлечете всички записи от таблица products, където цената е по-висока от 50.
# o	Изведете резултата като Pandas DataFrame.


import sqlite3
import pandas as pd

# Път до SQLite базата данни
db_path = 'mydatabase.db'

# Свързване към SQLite базата данни
conn = sqlite3.connect(db_path)

# SQL заявка за извличане на продукти с цена над 50
query = """
SELECT * FROM products
WHERE price > 50;
"""

# Изпълнение на заявката и зареждане в DataFrame
df = pd.read_sql_query(query, conn)

# Затваряме връзката с базата
conn.close()

# Показваме резултата
print("Продукти с цена над 50:")
print(df)


