# Имате DataFrame с колони 'дата' и 'приход'. Напишете код, който сортира DataFrame-а първо по дата (възходящо), а след това по приход (низходящо).

import pandas as pd

# Примерен DataFrame
df = pd.DataFrame({
    'дата': pd.to_datetime(['2023-05-01', '2023-04-01', '2023-05-01', '2023-04-01']),
    'приход': [1000, 1200, 1500, 800]
})

# Сортиране първо по 'дата' (възходящо), после по 'приход' (низходящо)
df_sorted = df.sort_values(by=['дата', 'приход'], ascending=[True, False])

print(df_sorted)
