#39.	Даден е категориален Series с цветове ('червен', 'син', 'зелен', 'червен'). Напишете код, който преименува категория 'червен' на 'ален'.

import pandas as pd

# Създаване на категориален Series
colors = pd.Series(pd.Categorical(['червен', 'син', 'зелен', 'червен']))

# Преименуване на категорията 'червен' на 'ален'
colors = colors.cat.rename_categories({'червен': 'ален'})

print(colors)
