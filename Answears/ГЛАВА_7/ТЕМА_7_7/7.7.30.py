# 30.	Имате DataFrame с колона 'категория' (низови стойности с много повторения). Напишете код, който преобразува тази колона в категориален тип.

import pandas as pd

# Примерен DataFrame с колона 'категория', съдържаща много повторения
df = pd.DataFrame({
    'продукт': ['A', 'B', 'C', 'A', 'B', 'C', 'A', 'B'],
    'категория': ['Храна', 'Напитка', 'Храна', 'Храна', 'Напитка', 'Храна', 'Храна', 'Напитка']
})

# Преобразуване на колоната 'категория' в категориален тип
df['категория'] = df['категория'].astype('category')

print(df)
print("\nТип на колоната 'категория':", df['категория'].dtype)
