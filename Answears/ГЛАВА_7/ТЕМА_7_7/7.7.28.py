# Имате DataFrame с колона 'дата' във формат 'гггг-мм-дд'. Напишете код, който преобразува тази колона в datetime тип.

import pandas as pd

# Примерен DataFrame с колона 'дата' като низове
df = pd.DataFrame({
    'дата': ['2023-01-01', '2023-02-15', '2023-03-30'],
    'стойност': [100, 200, 150]
})

# Преобразуване на колоната 'дата' към datetime тип
df['дата'] = pd.to_datetime(df['дата'], format='%Y-%m-%d')

print(df)
print("\nТип на колоната 'дата':", df['дата'].dtype)
