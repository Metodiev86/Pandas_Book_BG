#27.	Даден е Series с числови стойности, прочетени като низове. Напишете код, който ги преобразува в числа (float), като обработи евентуални грешки, замествайки ги с NaN.

import pandas as pd

# Примерен Series със стойности като низове, включително и невалидна стойност
data = pd.Series(['10.5', '20', 'текст', '30.1', 'NaN'])

# Преобразуване към float, като невалидните стойности се заменят с NaN
numeric_data = pd.to_numeric(data, errors='coerce')

print(numeric_data)
