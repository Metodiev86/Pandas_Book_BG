#35.	Имате DataFrame с колона 'време' (datetime тип). Напишете код, който създава нова колона 'ден_от_седмицата' (текст), съдържаща името на деня от седмицата за всяка дата.

import pandas as pd

# Примерен DataFrame с datetime колона 'време'
data = {
    'време': pd.to_datetime([
        '2023-01-01', '2023-05-15', '2023-11-25', '2023-07-30'
    ])
}

df = pd.DataFrame(data)

# Създаване на нова колона 'ден_от_седмицата', съдържаща името на деня от седмицата
df['ден_от_седмицата'] = df['време'].dt.day_name()

print(df)
