# 19.	Даден е DataFrame с колони, които са числови индекси (0, 1, 2). Напишете код, който ги преименува на 'Колона A', 'Колона B', 'Колона C', използвайки .set_axis().

import pandas as pd

# Създаваме DataFrame с числови индекси като имена на колони
df = pd.DataFrame([
    [1, 2, 3],
    [4, 5, 6]
])

# Преименуваме колоните с .set_axis() и задаваме axis=1 (за колони)
df = df.set_axis(['Колона A', 'Колона B', 'Колона C'], axis=1)

print(df)
