#Имате DataFrame с колона 'име'. Напишете код, който създава нова колона 'първа_буква', съдържаща първата буква от всяко име, използвайки .apply() с lambda функция.

import pandas as pd

# Примерен DataFrame с колона 'име'
df = pd.DataFrame({'име': ['Алиса', 'Борис', 'Стоян', 'Галя']})

# Извличане на първата буква чрез .apply() и lambda функция
df['първа_буква'] = df['име'].apply(lambda x: x[0])

print(df)
