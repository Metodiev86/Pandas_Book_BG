# Даден е DataFrame с колони 'цена1', 'цена2', 'цена3'. Напишете код, който създава нова колона 'средна_цена', съдържаща средната стойност от тези три колони за всеки ред, използвайки .apply() с axis=1.

import pandas as pd

# Примерен DataFrame с три ценови колони
df = pd.DataFrame({
    'цена1': [100, 150, 200],
    'цена2': [110, 140, 210],
    'цена3': [105, 145, 205]
})

# Изчисляване на средната стойност по редове чрез .apply() с axis=1
df['средна_цена'] = df.apply(lambda row: (row['цена1'] + row['цена2'] + row['цена3']) / 3, axis=1)

print(df)
