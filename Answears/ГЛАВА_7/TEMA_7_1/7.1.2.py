#2.	Имате Series с резултати от тест (числа). Напишете код, който връща булев Series, указващ кои резултати са по-високи от средната стойност на всички резултати.

import pandas as pd

# Примерен Series с резултати от тест
results = pd.Series([75, 82, 90, 68, 88, 79, 95])

# Изчисляване на средната стойност
avg = results.mean()

# Булев Series с True за резултати над средното
above_avg = results > avg

print("Резултати над средното:\n", above_avg)
