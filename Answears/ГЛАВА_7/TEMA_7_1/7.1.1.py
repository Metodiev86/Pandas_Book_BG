#1.	Даден е DataFrame с колони 'A' и 'B' с числови стойности. Напишете код, който създава нова колона 'C', съдържаща сумата на стойностите от колони 'A' и 'B'.

import pandas as pd

# Създаваме примерен DataFrame с числови стойности в колони 'A' и 'B'
df = pd.DataFrame({
    'A': [10, 20, 30, 40],
    'B': [1, 2, 3, 4]
})

# Показваме оригиналния DataFrame
print("Оригинален DataFrame:")
print(df)

# Създаваме нова колона 'C', която е поелементна сума на колоните 'A' и 'B'
df['C'] = df['A'] + df['B']

# Извеждаме резултата с новата колона
print("\nDataFrame със създадена колона 'C' (A + B):")
print(df)
