#3.	Даден е DataFrame с колони 'статус' (текст) и 'цена' (числа). Напишете код, който филтрира DataFrame-а, запазвайки само редовете, където статусът е 'Активен' и цената е по-малка от 100.

import pandas as pd

# Примерен DataFrame
df = pd.DataFrame({
    'статус': ['Активен', 'Неактивен', 'Активен', 'Активен', 'Неактивен'],
    'цена': [80, 120, 50, 150, 90]
})

# Филтриране на редовете: статус == 'Активен' и цена < 100
filtered_df = df[(df['статус'] == 'Активен') & (df['цена'] < 100)]

print(filtered_df)
