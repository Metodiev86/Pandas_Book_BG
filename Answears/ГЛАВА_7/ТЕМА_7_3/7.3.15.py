# Даден е DataFrame с колона 'резултат' (числа). Напишете код, който създава нова колона 'статус', която е 'Успех', ако резултатът е >= 60, и 'Провал' в противен случай (използвайте np.where).

import pandas as pd
import numpy as np

# Примерен DataFrame с колона 'резултат'
df = pd.DataFrame({
    'резултат': [55, 70, 90, 45, 60]
})

# Използваме np.where за да създадем нова колона 'статус'
df['статус'] = np.where(df['резултат'] >= 60, 'Успех', 'Провал')

print(df)
