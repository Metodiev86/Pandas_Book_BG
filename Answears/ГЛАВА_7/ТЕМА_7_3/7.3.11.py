# Имате DataFrame с колони 'брой' и 'единична_цена'. Напишете код, който създава нова колона 'обща_стойност', като умножава стойностите от тези две колони.

import pandas as pd

# Примерен DataFrame
df = pd.DataFrame({
    'брой': [5, 10, 3, 7],
    'единична_цена': [20.0, 15.5, 30.0, 12.0]
})

# Създаване на нова колона 'обща_стойност', като умножаваме стойностите от колоните 'брой' и 'единична_цена'
df['обща_стойност'] = df['брой'] * df['единична_цена']

print(df)
