# Имате DataFrame с колона 'категория' и 'цена'. Напишете код, който създава нова колона 'отстъпка', която е 10% от цената, само за редовете, където категорията е 'Промоция' (използвайте .loc).

import pandas as pd

# Примерен DataFrame с колони 'категория' и 'цена'
df = pd.DataFrame({
    'категория': ['Промоция', 'Стандарт', 'Промоция', 'Стандарт'],
    'цена': [100, 150, 80, 200]
})

# Използваме .loc за да добавим нова колона 'отстъпка' само за редовете с категория 'Промоция'
df.loc[df['категория'] == 'Промоция', 'отстъпка'] = df['цена'] * 0.10

#Заместваме липсващите стойности с 0, като променяме оригиналния DataFrame
df.fillna(value=0, inplace=True)

print(df)
