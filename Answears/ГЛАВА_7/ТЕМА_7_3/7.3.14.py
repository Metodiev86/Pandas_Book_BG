# Имате DataFrame с колони 'първо_име' и 'фамилия'. Използвайте .assign() за да добавите нова колона 'пълно_име', съдържаща обединените първо и фамилно име (разделени с интервал).

import pandas as pd

# Примерен DataFrame с колони 'първо_име' и 'фамилия'
df = pd.DataFrame({
    'първо_име': ['Иван', 'Мария', 'Георги', 'Елена'],
    'фамилия': ['Иванов', 'Петрова', 'Димитров', 'Стефанова']
})

# Използваме .assign() за да добавим нова колона 'пълно_име', която е обединение на 'първо_име' и 'фамилия'
df = df.assign(пълно_име=df['първо_име'] + ' ' + df['фамилия'])

print(df)
