#Даден е DataFrame с колона 'дата_раждане' (datetime тип). Напишете код, който създава нова колона 'година_раждане', съдържаща годината от датата на раждане.

import pandas as pd

# Примерен DataFrame с колона 'дата_раждане'
df = pd.DataFrame({
    'дата_раждане': ['1990-05-21', '1985-03-10', '2000-08-15', '1995-11-30']
})

# Преобразуваме колоната 'дата_раждане' към datetime тип
df['дата_раждане'] = pd.to_datetime(df['дата_раждане'])

# Създаване на нова колона 'година_раждане', като извличаме годината от 'дата_раждане'
df['година_раждане'] = df['дата_раждане'].dt.year

print(df)
