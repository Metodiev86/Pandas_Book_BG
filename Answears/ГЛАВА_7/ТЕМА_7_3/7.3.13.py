# Даден е DataFrame с колона 'цена'. Използвайте .assign() за да добавите нова колона 'цена_с_ддс', която е цената, умножена по 1.20.

import pandas as pd

# Примерен DataFrame с колона 'цена'
df = pd.DataFrame({
    'цена': [100, 200, 150, 80, 50]
})

# Използваме .assign() за да добавим нова колона 'цена_с_ддс', която е цената * 1.20
df = df.assign(цена_с_ддс=df['цена'] * 1.20)

print(df)
