#40.	Даден е DataFrame. Използвайте .pipe() за да приложите последователно две функции: първата, която филтрира редовете, където стойността в колона 'А' е по-голяма от 10, и втората, която преименува колона 'Б' на 'Ново име'.

#Решение с lambda функции директно в pipe(), без нужда от именувани функции.

import pandas as pd

# Примерен DataFrame
df = pd.DataFrame({
    'А': [5, 15, 25],
    'Б': [100, 200, 300]
})

# Използване на .pipe() с lambda функции
result = (
    df
    .pipe(lambda d: d[d['А'] > 10])               # Филтрираме редовете където 'А' > 10
    .pipe(lambda d: d.rename(columns={'Б': 'Ново име'}))  # Преименуваме 'Б' на 'Ново име'
)

print(result)

