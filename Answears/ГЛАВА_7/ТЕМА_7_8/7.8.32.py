#32.	Имате DataFrame с колона 'текст'. Напишете код, който преброява колко пъти се среща думата 'важно' (без значение от регистъра) във всеки текст.

import pandas as pd

# Примерен DataFrame с колона 'текст'
data = {'текст': [
    'Това е много важно съобщение.',
    'Този текст не съдържа ключова дума.',
    'Важно е да знаеш, че това е важно.',
    'Няма нужда да се тревожиш.'
]}
df = pd.DataFrame(data)

# Преброяване на срещанията на думата 'важно' (без значение от регистъра)
df['брой_важно'] = df['текст'].str.lower().str.count('важно')

print(df)
