#33.	Даден е Series с телефонни номера в различни формати. Напишете код, който ги стандартизира, като премахне всички нецифрови символи.

import pandas as pd

# Примерен Series с телефонни номера в различни формати
phone_numbers = pd.Series([
    '(123) 456-7890',
    '123.456.7890',
    '+1-123-456-7890',
    '1234567890'
])

# Стандартизиране на телефонните номера чрез премахване на всички нецифрови символи
standardized_phone_numbers = phone_numbers.str.replace(r'\D', '', regex=True)

print(standardized_phone_numbers)
